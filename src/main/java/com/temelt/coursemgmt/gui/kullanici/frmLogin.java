    private void btnGirisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGirisActionPerformed
       String kullaniciAdi = txtKullaniciAdi.getText();
        String sifre =txtSifre.getText();
        Kullanici kullanici = kullaniciService.getByUsernameAndPassword(kullaniciAdi, sifre);
        if(kullanici!=null){
            frmAnaSayfa anaSayfa=new frmAnaSayfa(kullanici);
            anaSayfa.show();
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Kullanıc Adı veya Şifre Hatalı");
        }
        
    }//GEN-LAST:event_btnGirisActionPerformed

