    /**
     * Bu method veritabanı 
     * ilk oluşturuldugunda 
     * sisteme kullanıcıları ve rolleri ekler 
     */
    private void kullaniciKontrol() {
        List<Kullanici> liste = kullaniciService.getAll();
        if(liste==null || liste.size()==0){
       
            Kullanici k=new Kullanici();
            k.setAd("Taner");
            k.setEmail("taner@temel.com");
            k.setKullaniciAdi("admin");
            k.setSifre("1");
            k.setSoyad("TEMEL");
            k.setUyelikTarihi(new Date());
            k.setRol(Rol.ADMIN);
            kullaniciService.save(k);
        }
    }
